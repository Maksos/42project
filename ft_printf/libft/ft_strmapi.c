#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned int	i;
	char			*new;

	if (!s || !f)
		return (NULL);
	new = malloc(sizeof(char) * (ft_strlen(s) + 1));
	if (!new)
		return (NULL);
	i = 0;
	while (s && s[i])
	{
		new[i] = f(i, s[i]);
		if (new[i] == 0)
			return (NULL);
		i++;
	}
	new[i] = '\0';
	return (new);
}
