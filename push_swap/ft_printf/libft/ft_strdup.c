#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int			i;
	char		*new;

	i = 0;
	while (s1[i])
		i++;
	new = malloc(sizeof(char) * (i + 1));
	if (new == NULL)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		new[i] = s1[i];
		i++;
	}
	new[i] = '\0';
	return (new);
}
